package bean;

public class Characters {
private char sequence;
private int count;
public Characters() {
	
}
public char getSequence() {
	return sequence;
}
public Characters(char sequence, int count) {
	
	this.sequence = sequence;
	this.count = count;
}

public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}


}
