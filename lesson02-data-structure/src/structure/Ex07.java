package structure;

public class Ex07 {
	public static void main(String[] args) {
		int a = 12;
		int b = 36;
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		System.out.println("=====AFTER SWAP=====");
		int tmp = a;
		a = b;
		b = tmp;
		
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		
	}

}
